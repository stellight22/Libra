#include <iostream> 

using namespace std;

int main()
{

    //Integer Declaration
    int i; // Values can take -2147483648 to 2147483647
    short int i1;
    long int i2;
    long long int i3;

    //Signed Integer Declaration
    signed int i4; // Values can take -2147483648 to 2147483647
    signed short int i5;
    signed long int i6;
    signed long long int i7;

    //Unsigned Integer Declaration
    unsigned int i8; //	Values can take 0 to 4294967295  2^(32) = 4294967296, but -1 bc we start at 0
    unsigned short int i9;
    unsigned long int i10;
    unsigned long long int i11;

    //Float Declaration
    float f;

    // Character Declaration
    char c;

    //Double Declaration
    double d;

    // Multiple Variable Declaration of the Same Type
    int x, y, z;

    return 0;
}